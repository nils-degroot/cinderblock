// # SQLx Extension Proc Macro
//
// This proc macro is invoked by the `resource!` macro when a resource declares
// `cinderblock_sqlx` in its `extensions { ... }` block. It receives the full resource
// DSL tokens plus the extension-specific config, and generates an
// `impl SqlResource for <Resource>` block.
//
// # Config syntax
//
// ```text
// cinderblock_sqlx { table = "tickets"; }
// ```
//
// The only required config key is `table`, which specifies the SQLite table
// name for the resource.
//
// # Generated columns
//
// Attributes marked `generated true` are omitted from INSERT and UPDATE
// statements. Their values come from the database (e.g. autoincrement
// primary keys, server-side DEFAULT expressions). The column is still
// included in `COLUMN_NAMES` and `from_row` so that SELECT and RETURNING
// results decode correctly.
//
// # Generated code
//
// For a resource `Ticket` with attributes `ticket_id` (generated),
// `subject`, `status` (where `ticket_id` is the primary key), the macro
// generates:
//
// ```rust,ignore
// impl cinderblock_sqlx::SqlResource for Ticket {
//     const TABLE_NAME: &'static str = "tickets";
//     const COLUMN_NAMES: &'static [&'static str] = &["ticket_id", "subject", "status"];
//     const INSERT_COLUMN_NAMES: &'static [&'static str] = &["subject", "status"];
//     const PRIMARY_KEY_COLUMN: &'static str = "ticket_id";
//
//     fn bind_insert(&self, builder: &mut cinderblock_sqlx::sqlx::QueryBuilder<'_, cinderblock_sqlx::sqlx::Sqlite>) {
//         let mut sep = builder.separated(", ");
//         // ticket_id omitted — generated by database
//         sep.push_bind(self.subject.clone());
//         sep.push_bind(self.status.clone());
//     }
//
//     fn bind_update(&self, builder: &mut cinderblock_sqlx::sqlx::QueryBuilder<'_, cinderblock_sqlx::sqlx::Sqlite>) {
//         let mut sep = builder.separated(", ");
//         // ticket_id omitted — primary key and generated
//         sep.push("subject = ").push_bind_unseparated(self.subject.clone());
//         sep.push("status = ").push_bind_unseparated(self.status.clone());
//     }
//
//     fn bind_primary_key(
//         pk: &<Self as cinderblock_core::Resource>::PrimaryKey,
//         builder: &mut cinderblock_sqlx::sqlx::QueryBuilder<'_, cinderblock_sqlx::sqlx::Sqlite>,
//     ) {
//         builder.push_bind(pk.clone());
//     }
//
//     fn from_row(row: &cinderblock_sqlx::sqlx::sqlite::SqliteRow) -> cinderblock_core::Result<Self> {
//         use cinderblock_sqlx::sqlx::Row;
//         Ok(Self {
//             ticket_id: row.try_get("ticket_id")
//                 .map_err(|e| format!("decode column `ticket_id`: {e}"))?,
//             subject: row.try_get("subject")
//                 .map_err(|e| format!("decode column `subject`: {e}"))?,
//             status: row.try_get("status")
//                 .map_err(|e| format!("decode column `status`: {e}"))?,
//         })
//     }
// }
// ```

use cinderblock_extension_api::{ExtensionMacroInput, ReadFilterValue};
use syn::{parse::Parse, Ident, LitStr, Type};

/// Checks whether a `syn::Type` is `Option<T>`.
fn is_option_type(ty: &Type) -> bool {
    if let Type::Path(type_path) = ty {
        type_path
            .path
            .segments
            .last()
            .is_some_and(|seg| seg.ident == "Option")
    } else {
        false
    }
}

